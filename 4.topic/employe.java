
// Object Initialization using Reference Variable
public class employe {
    int empid;
    String empname;


    public static void main(String[] args){
        employe e = new employe();
        e.empid = 101;
        e.empname = "summit ";

        System.out.println(e.empid);
        System.out.println(e.empname);
    }
    
}
